<script type="text/javascript">
//search bar function
$("#term").bind('blur keyup',function(e) {
    if (e.type === 'blur' || e.keyCode === 13){
        let _term = $("#term").val();
        if(_term != null && _term.trim() !== ''){
            window.location = '/search/'+_term;
        }
    }
});

</script>

